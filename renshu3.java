public class renshu3 {
    public static void main(String[] args) {
        int a = 52;        
        int b = 48;
        int c = 60;
        if(a > 50){
            System.out.println("大きい");
        }else{
            System.out.println("小さい");
        }
        if(b < 50){
            System.out.println("小さい");
        }else{
            System.out.println("大きい");
        }
        if(a != b){
            System.out.println("等しくない");
        }else{
            System.out.println("等しく");
        }
        if(c > 50){
            System.out.println("50より大きい");
        }else{
            System.out.println("50より小さい");
        }
    }
}